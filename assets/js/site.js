/* Shared rendering for all pages.
   Content is loaded from the JSON files in /data (managed by the /admin panel).
   If those can't be fetched (e.g. opening index.html directly from disk), it falls
   back to the defaults in data.js so local double-click preview still works.
   You normally do NOT edit this file. */
(function () {
  var S = {};

  var NAV = [
    { label: "Home", href: "index.html" },
    { label: "About", href: "about.html" },
    { label: "Books", href: "books.html" },
    { label: "Works in Progress", href: "works-in-progress.html" },
    { label: "The Whisperer", href: "the-whisperer.html" },
    { label: "Media / Visual Inspiration", href: "media.html" },
    { label: "News / Updates", href: "news.html" },
    { label: "Contact", href: "contact.html" }
  ];

  function slug(s) { return (s || "").toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, ""); }
  function esc(s) { return (s == null ? "" : String(s)).replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;"); }
  function nl2br(s) { return esc(s).replace(/\n/g, "<br>"); }

  function badge(status) {
    if (!status) return "";
    return '<span class="badge badge-' + slug(status) + '">' + esc(status) + "</span>";
  }

  function img(path, alt, cls) {
    if (path) return '<img src="' + esc(path) + '" alt="' + esc(alt) + '" loading="lazy" class="' + cls + '">';
    return '<div class="placeholder ' + cls + '"><span>Image</span></div>';
  }

  function gallery(list) {
    list = list || [];
    if (!list.length) return '<div class="gallery-empty">Inspiration images will appear here. Add them in the admin panel.</div>';
    return '<div class="gallery">' + list.map(function (p) {
      return '<a class="gallery-item" href="' + esc(p) + '" target="_blank" rel="noopener">' + img(p, "Inspiration image", "") + "</a>";
    }).join("") + "</div>";
  }

  function purchaseButtons(b) {
    var out = [];
    (b.purchase_links || []).forEach(function (l) {
      if (l && l.url) out.push('<a class="btn btn-outline" href="' + esc(l.url) + '" target="_blank" rel="noopener">' + esc(l.label) + "</a>");
    });
    if (b.audiobook_link) out.push('<a class="btn btn-outline" href="' + esc(b.audiobook_link) + '" target="_blank" rel="noopener">Audiobook</a>');
    if (b.trailer_link) out.push('<a class="btn btn-ghost" href="' + esc(b.trailer_link) + '" target="_blank" rel="noopener">Watch Trailer</a>');
    if (!out.length) return '<p class="muted">Purchase links will be added here soon.</p>';
    return '<div class="btn-row">' + out.join("") + "</div>";
  }

  /* ---------- header / footer ---------- */
  function currentFile() {
    var p = location.pathname.split("/").pop();
    return p || "index.html";
  }
  function renderHeader() {
    var cur = currentFile();
    var links = NAV.map(function (n) {
      var active = n.href === cur ? " active" : "";
      return '<a class="navlink' + active + '" href="' + n.href + '">' + esc(n.label) + "</a>";
    }).join("");
    var h = document.getElementById("site-header");
    if (!h) return;
    h.innerHTML =
      '<div class="container nav">' +
        '<a class="brand" href="index.html">\u270e David Goodman</a>' +
        '<button class="menu-toggle" aria-label="Toggle menu">\u2630</button>' +
        '<nav class="navlinks">' + links + "</nav>" +
      "</div>";
    var btn = h.querySelector(".menu-toggle");
    var nav = h.querySelector(".navlinks");
    if (btn && nav) btn.addEventListener("click", function () { nav.classList.toggle("open"); });
  }
  function renderFooter() {
    var f = document.getElementById("site-footer");
    if (!f) return;
    var ft = S.footer || {};
    var links = [["Home", "index.html"], ["About", "about.html"], ["Books", "books.html"], ["Works in Progress", "works-in-progress.html"], ["Contact", "contact.html"]]
      .map(function (l) { return '<a href="' + l[1] + '">' + l[0] + "</a>"; }).join("");
    f.innerHTML =
      '<div class="container footer-grid">' +
        '<div><p class="footer-name">' + esc(ft.name) + '</p><p class="muted">' + esc(ft.domain) + '</p>' +
          '<a class="footer-email" href="mailto:' + esc(ft.email) + '">' + esc(ft.email) + "</a></div>" +
        '<div class="footer-links">' + links +
          '<p class="muted copyright">\u00a9 ' + new Date().getFullYear() + " " + esc(ft.name) + ". All rights reserved.</p></div>" +
      "</div>";
  }

  /* ---------- page renderers ---------- */
  function findProject(slugName) { return (S.projects || []).filter(function (p) { return p.slug === slugName; })[0]; }

  function renderHome() {
    var h = S.home || {};
    var book = (S.books || [])[0];
    var whisperer = findProject("the-whisperer");
    var vampire = findProject("vampire-novel");
    function card(eyebrow, p, href) {
      if (!p) return "";
      return '<a class="feature-card" href="' + href + '">' +
        '<p class="eyebrow">' + esc(eyebrow) + "</p>" +
        '<h3>' + esc(p.title) + "</h3>" +
        '<div class="meta">' + badge(p.status) + (p.genre ? '<span class="muted">' + esc(p.genre) + "</span>" : "") + "</div>" +
        '<p class="card-desc">' + esc(p.short_description) + "</p>" +
        '<span class="link-more">Learn more \u2192</span></a>';
    }
    return '' +
      '<section class="hero"><div class="container hero-grid">' +
        '<div class="hero-text">' +
          '<p class="eyebrow gold">' + esc(h.hero_subtitle) + "</p>" +
          '<h1>' + esc(h.hero_title) + "</h1>" +
          '<p class="lead">' + esc(h.hero_tagline) + "</p>" +
          '<div class="btn-row">' +
            '<a class="btn btn-primary" href="books.html">View Books</a>' +
            '<a class="btn btn-outline" href="works-in-progress.html">Current Projects</a>' +
            '<a class="btn btn-ghost" href="contact.html">Contact David</a>' +
          "</div></div>" +
        '<div class="hero-photo">' + img(h.author_photo, "David Goodman", "portrait") + "</div>" +
      "</div></section>" +
      '<section class="section"><div class="container reading">' +
        '<h2>' + esc(h.welcome_heading) + "</h2>" +
        '<p class="body-text">' + nl2br(h.welcome_text) + "</p>" +
      "</div></section>" +
      '<section class="section alt"><div class="container">' +
        "<h2>Featured Work</h2>" +
        '<div class="feature-grid">' +
          card("Featured Book", book, "books.html") +
          card("Current Project", whisperer, "the-whisperer.html") +
          card("Future Project", vampire, "works-in-progress.html") +
        "</div></div></section>";
  }

  function renderAbout() {
    var a = S.about || {};
    function block(t, x) { return x ? '<h2>' + esc(t) + "</h2><p class=\"body-text\">" + nl2br(x) + "</p>" : ""; }
    return '' +
      '<section class="page-head"><div class="container"><h1>About David Goodman</h1></div></section>' +
      '<section class="section"><div class="container about-grid">' +
        '<div class="about-photo">' + img(a.portrait, "David Goodman portrait", "portrait") + "</div>" +
        '<div class="about-text">' +
          '<p class="body-text lead-text">' + nl2br(a.bio) + "</p>" +
          block("A Personal Note", a.personal_note) +
          block("My Writing Journey", a.writing_journey) +
          block("Self-Publishing Experience", a.self_publishing) +
          block("Looking Ahead", a.future_goals) +
        "</div></div></section>";
  }

  function renderBooks() {
    var items = (S.books || []).map(function (b) {
      return '<article class="book-card">' +
        '<div class="book-grid">' +
          '<div class="book-cover">' + img(b.cover, b.title + " cover", "cover") + "</div>" +
          '<div class="book-body">' +
            '<h2>' + esc(b.title) + "</h2>" +
            '<div class="meta">' + badge(b.status) + (b.genre ? '<span class="muted">' + esc(b.genre) + "</span>" : "") + "</div>" +
            '<p class="body-text">' + esc(b.short_description) + "</p>" +
            (b.long_description ? '<p class="body-text">' + nl2br(b.long_description) + "</p>" : "") +
            (b.back_cover_description ? '<p class="body-text muted">' + nl2br(b.back_cover_description) + "</p>" : "") +
            purchaseButtons(b) +
            (b.audiobook_update ? '<p class="note">' + esc(b.audiobook_update) + "</p>" : "") +
          "</div></div>" +
          (b.excerpt ? '<div class="excerpt"><h3>Sample Excerpt</h3><p class="body-text italic">' + nl2br(b.excerpt) + "</p></div>" : "") +
          '<div class="book-gallery"><h3>Visual Inspiration</h3>' + gallery(b.gallery) + "</div>" +
        "</article>";
    }).join("");
    return '<section class="page-head"><div class="container"><h1>Books</h1><p class="lead">Completed and published works by David Goodman.</p></div></section>' +
      '<section class="section"><div class="container stack">' + (items || '<p class="muted">No books yet.</p>') + "</div></section>";
  }

  function projectCard(p) {
    var themes = (p.themes || []).map(function (t) { return '<span class="chip">' + esc(t) + "</span>"; }).join("");
    var imgs = (p.character_images || []).concat(p.inspiration_images || []);
    return '<article class="project-card">' +
      '<div class="project-head"><h2>' + esc(p.title) + "</h2>" + badge(p.status) + "</div>" +
      (p.genre ? '<p class="muted">' + esc(p.genre) + "</p>" : "") +
      '<p class="body-text">' + esc(p.short_description) + "</p>" +
      (themes ? '<div class="chips">' + themes + "</div>" : "") +
      (p.progress_notes ? '<p class="note">' + esc(p.progress_notes) + "</p>" : "") +
      purchaseButtons(p) +
      (p.excerpt ? '<details class="excerpt-toggle"><summary>Read excerpt</summary><p class="body-text italic">' + nl2br(p.excerpt) + "</p></details>" : "") +
      (imgs.length ? '<div class="book-gallery">' + gallery(imgs) + "</div>" : "") +
      "</article>";
  }

  function renderWIP() {
    var list = (S.projects || []).filter(function (p) { return p.show_in_wip; });
    var cards = list.map(projectCard).join("");
    return '<section class="page-head forest"><div class="container"><h1>Works in Progress</h1><p class="lead">Projects in development \u2014 some early, some close. Updates posted as they grow.</p></div></section>' +
      '<section class="section"><div class="container project-grid">' + (cards || '<p class="muted">No works in progress yet.</p>') + "</div></section>";
  }

  function renderWhisperer() {
    var p = findProject("the-whisperer");
    if (!p) return '<section class="section"><div class="container"><p>Not found.</p></div></section>';
    var themes = (p.themes || []).map(function (t) { return '<span class="chip">' + esc(t) + "</span>"; }).join("");
    return '' +
      '<section class="page-head forest"><div class="container">' +
        '<p class="eyebrow forest-eyebrow">Children\'s Picture Book</p>' +
        '<h1>' + esc(p.title) + "</h1>" +
        '<div class="meta">' + badge(p.status) + '<span class="muted">Seeking Traditional Publishing</span></div>' +
        '<p class="lead">' + esc(p.long_description || p.short_description) + "</p>" +
      "</div></section>" +
      '<section class="section"><div class="container stack">' +
        (themes ? '<div><h2>Themes</h2><div class="chips">' + themes + "</div></div>" : "") +
        '<div><h2>Dan \u2014 Character Inspiration</h2>' + gallery(p.character_images) + "</div>" +
        '<div><h2>The Forest &amp; World</h2>' + gallery(p.inspiration_images) + "</div>" +
        (p.excerpt ? '<div><h2>Sample Manuscript</h2><p class="body-text italic">' + nl2br(p.excerpt) + "</p></div>" : "") +
        (p.progress_notes ? '<div><h2>Query &amp; Publishing Status</h2><p class="note">' + esc(p.progress_notes) + "</p></div>" : "") +
        '<div class="disclaimer">' + esc(p.disclaimer) + "</div>" +
      "</div></section>";
  }

  function renderMedia() {
    var m = S.media || {};
    var secs = (m.sections || []).map(function (s) {
      return '<div class="media-section"><h2>' + esc(s.name) + "</h2>" + gallery(s.images) + "</div>";
    }).join("");
    return '<section class="page-head"><div class="container"><h1>Media / Visual Inspiration</h1><p class="lead">Concept art, character &amp; scene inspiration, mood boards, and promotional imagery.</p></div></section>' +
      '<section class="section"><div class="container">' +
        '<div class="info-box">' + esc(m.note) + "</div>" +
        '<div class="stack">' + secs + "</div>" +
      "</div></section>";
  }

  function renderNews() {
    var posts = (S.news || []).map(function (n) {
      return '<article class="news-card">' +
        '<div class="news-meta"><span class="chip">' + esc(n.category) + "</span><span class=\"muted\">" + esc(n.date) + "</span></div>" +
        '<h2>' + esc(n.title) + "</h2>" +
        '<p class="body-text">' + nl2br(n.body) + "</p></article>";
    }).join("");
    return '<section class="page-head"><div class="container"><h1>News / Updates</h1><p class="lead">Book updates, query news, audiobook progress, and announcements.</p></div></section>' +
      '<section class="section"><div class="container stack">' + (posts || '<p class="muted">No updates yet.</p>') + "</div></section>";
  }

  function renderContact() {
    var c = S.contact || {};
    var extra = [];
    if (c.amazon_author_url) extra.push('<a href="' + esc(c.amazon_author_url) + '" target="_blank" rel="noopener">Amazon Author Page</a>');
    (c.social_links || []).forEach(function (l) { if (l && l.url) extra.push('<a href="' + esc(l.url) + '" target="_blank" rel="noopener">' + esc(l.label) + "</a>"); });
    return '<section class="page-head"><div class="container"><h1>Contact</h1>' +
        '<p class="lead">' + esc(c.intro) + "</p>" +
        '<a class="contact-email" href="mailto:' + esc(c.email) + '">' + esc(c.email) + "</a></div></section>" +
      '<section class="section"><div class="container contact-grid">' +
        '<div class="contact-card"><h2>Send a message</h2>' +
          '<p class="body-text">Click below to email David directly from your own email app.</p>' +
          '<a class="btn btn-primary" href="mailto:' + esc(c.email) + '?subject=Message%20via%20DavidGoodmanAuthor.com">Email David</a></div>' +
        '<div class="contact-card"><h2>Connect</h2>' +
          '<a class="contact-link" href="mailto:' + esc(c.email) + '">' + esc(c.email) + "</a>" +
          (extra.length ? extra.map(function (e) { return '<div class="contact-link">' + e + "</div>"; }).join("") : '<p class="muted">Social media and author links will be added here.</p>') +
        "</div></div></section>";
  }

  var renderers = {
    home: renderHome, about: renderAbout, books: renderBooks, wip: renderWIP,
    whisperer: renderWhisperer, media: renderMedia, news: renderNews, contact: renderContact
  };

  /* ---------- content loader (JSON files managed by /admin) ---------- */
  function loadFromJSON() {
    var files = {
      home: "data/home.json", about: "data/about.json", contact: "data/contact.json",
      footer: "data/footer.json", media: "data/media.json", books: "data/books.json",
      projects: "data/projects.json", news: "data/news.json"
    };
    var keys = Object.keys(files);
    return Promise.all(keys.map(function (k) {
      return fetch(files[k], { cache: "no-store" }).then(function (r) {
        if (!r.ok) throw new Error("Could not load " + files[k]);
        return r.json();
      });
    })).then(function (results) {
      var d = {};
      keys.forEach(function (k, i) { d[k] = results[i]; });
      return {
        home: d.home,
        about: d.about,
        contact: d.contact,
        footer: d.footer,
        media: d.media,
        books: (d.books && d.books.books) || [],
        projects: (d.projects && d.projects.projects) || [],
        news: (d.news && d.news.news) || []
      };
    });
  }

  function renderPage() {
    renderHeader();
    var page = document.body.getAttribute("data-page");
    var fn = renderers[page];
    var target = document.getElementById("page-content");
    if (fn && target) target.innerHTML = fn();
    renderFooter();
  }

  document.addEventListener("DOMContentLoaded", function () {
    loadFromJSON().then(function (data) {
      S = data;
      renderPage();
    }).catch(function () {
      // Fallback: use defaults from data.js (works when opening files directly from disk)
      S = window.SITE || {};
      renderPage();
    });
  });
})();
